package controlFlow_Statements;

public class Switch_Program {

	public static void main(String[] args) {
     int i=20;
     
     switch(i)
     {
         case 10:
    	 System.out.println("i am 10");
    	 break;
    	 
         case 20:
        	 System.out.println("i am 20");
        	 break;
        	 
         case 30:
        	 System.out.println("i am 30");
        	 break;
         
         case 40:
        	 System.out.println("i am 40");
        	 break;
        	 
         case 50:
        	 System.out.println("i am 50");
        	 break;
        	 
        	 default:
        		 System.out.println("i am nothing");
     }
	}

}
